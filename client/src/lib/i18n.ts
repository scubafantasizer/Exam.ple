export type TKey = keyof typeof translations.tr;

let currentLang: Lang = (localStorage.getItem("lang") as Lang) ?? "tr";
const listeners = new Set<() => void>();

export function getLang(): Lang { return currentLang; }
export function setLang(l: Lang) {
  currentLang = l;
  localStorage.setItem("lang", l);
  listeners.forEach(fn => fn());
}
export function onLangChange(fn: () => void) { listeners.add(fn); return () => listeners.delete(fn); }
export function t(key: TKey): string { return (translations[currentLang] as Record<string, string>)[key] ?? key; }
